import { afterEach, describe, expect, it } from "vitest";
import http from "node:http";
import os from "node:os";
import path from "node:path";
import { mkdtemp, readFile, rm, writeFile } from "node:fs/promises";

import { closeAllMounts } from "../src/adapter/runtime.js";
import {
  authorDatasetEnvironmentTemplate,
  DATASET_INIT_TEMPLATE_SKILL,
  type DatasetEnvironmentTemplate,
} from "../src/server/datasetTemplateAgent.js";
import { createServer } from "../src/server/server.js";
import type { DatasetTemplateAuthorInput } from "../src/server/datasetTemplateAgent.js";
import {
  getBodyDispatcher,
  setBodyDispatcher,
  type BodyDispatcher,
  type DispatchContext,
} from "../src/sdk/runtime.js";
import type { Body } from "../src/sdk/body.js";

const tempDirs: string[] = [];
const envBefore = process.env["HF_DATASETS_SERVER_URL"];

afterEach(async () => {
  await closeAllMounts();
  await Promise.all(tempDirs.map((dir) => rm(dir, { recursive: true, force: true })));
  tempDirs.length = 0;
  if (envBefore === undefined) delete process.env["HF_DATASETS_SERVER_URL"];
  else process.env["HF_DATASETS_SERVER_URL"] = envBefore;
});

describe("LLM-driven dataset environment initialization", () => {
  it("dispatches the default author through the datafetch init Flue skill", async () => {
    const prior = getBodyDispatcher();
    let seenBody: Body<unknown, unknown> | null = null;
    let seenCtx: DispatchContext | null = null;
    const fake: BodyDispatcher = {
      async dispatch<I, O>(body: Body<I, O>, _input: I, ctx: DispatchContext): Promise<O> {
        seenBody = body as Body<unknown, unknown>;
        seenCtx = ctx;
        const template = {
          agentsMd: "# agent template\n",
          scratchTs: "console.log('probe');\n",
          answerTs: "return df.answer({ status: 'unsupported', evidence: [], reason: 'todo' });\n",
        } satisfies DatasetEnvironmentTemplate;
        return template as unknown as O;
      },
    };

    try {
      setBodyDispatcher(fake);
      const template = await authorDatasetEnvironmentTemplate(
        minimalTemplateInput(),
        { model: "test-model" },
      );
      expect(template.agentsMd).toContain("agent template");
      expect(seenBody).toMatchObject({
        kind: "agent",
        skill: DATASET_INIT_TEMPLATE_SKILL,
        model: "test-model",
      });
      expect(seenCtx).toMatchObject({
        tenant: "__system__",
        mount: "opentraces-devtime",
        functionName: DATASET_INIT_TEMPLATE_SKILL,
      });
    } finally {
      setBodyDispatcher(prior);
    }
  });

  it("uses an agent-authored base mount template when the dataset opts in", async () => {
    const baseDir = await mkdtemp(path.join(os.tmpdir(), "df-init-agent-"));
    tempDirs.push(baseDir);
    const datasetsFile = path.join(baseDir, "datasets.json");
    const hf = await startFakeHfServer();
    let seenInput: DatasetTemplateAuthorInput | null = null;
    try {
      process.env["HF_DATASETS_SERVER_URL"] = hf.url;
      await writeFile(
        datasetsFile,
        `${JSON.stringify(
          {
            datasets: [
              {
                id: "opentraces-devtime",
                adapter: "huggingface",
                url: "https://huggingface.co/datasets/OpenTraces/opentraces-devtime",
                target: "open",
                init: "agent",
              },
            ],
          },
          null,
          2,
        )}\n`,
        "utf8",
      );

      const { app } = await createServer({
        baseDir,
        datasetsFile,
        templateAuthor: async (input) => {
          seenInput = input;
          return {
            agentsMd: [
              "# datafetch intent workspace",
              "",
              "Agent-authored OpenTraces guidance.",
              "Use `df.db.train.search` for trace probes before committing.",
              "",
            ].join("\n"),
            scratchTs: [
              "const rows = await df.db.train.search(\"debug\", { limit: 5 });",
              "console.log(JSON.stringify({ agentAuthored: true, count: rows.length }, null, 2));",
              "",
            ].join("\n"),
            answerTs: [
              "return df.answer({",
              "  status: \"unsupported\",",
              "  evidence: [],",
              "  reason: \"agent-authored template; implement the visible trajectory before commit\",",
              "});",
              "",
            ].join("\n"),
            notesMd: "# init notes\n\nGenerated by the dataset init agent.\n",
          };
        },
      });

      const res = await app.request("/v1/manifest");
      expect(res.status).toBe(200);
      const manifest = (await res.json()) as {
        datasets: Array<{ template?: { mode?: string; status?: string } }>;
      };
      expect(manifest.datasets[0]?.template).toMatchObject({
        mode: "agent",
        status: "accepted",
      });

      if (seenInput === null) {
        throw new Error("expected dataset template author to receive input");
      }
      const captured: DatasetTemplateAuthorInput = seenInput;
      expect(captured.dataset.id).toBe("opentraces-devtime");
      expect(captured.collections[0]).toMatchObject({
        ident: "train",
        name: "train",
        rows: 3,
      });
      expect(captured.samples.train?.[0]).toMatchObject({
        trace_id: "trace-1",
        text: "write docs",
      });

      const templateRoot = path.join(
        baseDir,
        "sources",
        "opentraces-devtime",
        "templates",
      );
      await expect(readFile(path.join(templateRoot, "AGENTS.md"), "utf8")).resolves.toContain(
        "Agent-authored OpenTraces guidance",
      );
      await expect(
        readFile(path.join(templateRoot, "scripts", "scratch.ts"), "utf8"),
      ).resolves.toContain("agentAuthored");
      await expect(readFile(path.join(templateRoot, "init-notes.md"), "utf8")).resolves.toContain(
        "Generated by the dataset init agent",
      );

      const initReport = JSON.parse(
        await readFile(
          path.join(baseDir, "sources", "opentraces-devtime", "init-agent.json"),
          "utf8",
        ),
      ) as { status: string; skill: string; source: string };
      expect(initReport).toMatchObject({
        status: "accepted",
        skill: "datafetch_init_mount_template",
        source: "agent",
      });
    } finally {
      await hf.close();
    }
  });

  it("falls back to deterministic templates when the init agent fails", async () => {
    const baseDir = await mkdtemp(path.join(os.tmpdir(), "df-init-agent-fallback-"));
    tempDirs.push(baseDir);
    const datasetsFile = path.join(baseDir, "datasets.json");
    const hf = await startFakeHfServer();
    try {
      process.env["HF_DATASETS_SERVER_URL"] = hf.url;
      await writeFile(
        datasetsFile,
        `${JSON.stringify(
          {
            datasets: [
              {
                id: "opentraces-devtime",
                adapter: "huggingface",
                url: "https://huggingface.co/datasets/OpenTraces/opentraces-devtime",
                init: "agent",
              },
            ],
          },
          null,
          2,
        )}\n`,
        "utf8",
      );

      const { app } = await createServer({
        baseDir,
        datasetsFile,
        templateAuthor: async () => {
          throw new Error("synthetic init-agent failure");
        },
      });

      const res = await app.request("/v1/manifest");
      expect(res.status).toBe(200);
      const manifest = (await res.json()) as {
        datasets: Array<{ template?: { mode?: string; status?: string; source?: string } }>;
      };
      expect(manifest.datasets[0]?.template).toMatchObject({
        mode: "agent",
        status: "fallback",
        source: "deterministic",
      });
      const templateRoot = path.join(
        baseDir,
        "sources",
        "opentraces-devtime",
        "templates",
      );
      await expect(readFile(path.join(templateRoot, "AGENTS.md"), "utf8")).resolves.toContain(
        "Dataset entry points",
      );
      const initReport = JSON.parse(
        await readFile(
          path.join(baseDir, "sources", "opentraces-devtime", "init-agent.json"),
          "utf8",
        ),
      ) as { status: string; error?: string };
      expect(initReport.status).toBe("fallback");
      expect(initReport.error).toContain("synthetic init-agent failure");
    } finally {
      await hf.close();
    }
  });
});

function minimalTemplateInput(): DatasetTemplateAuthorInput {
  return {
    version: 1,
    dataset: {
      id: "opentraces-devtime",
      title: "OpenTraces devtime",
      adapter: "huggingface",
      sourceUrl: "https://huggingface.co/datasets/OpenTraces/opentraces-devtime",
      target: "open",
    },
    adapterProfile: {
      capabilities: {
        lexical: true,
      },
      evidenceRefShape: "hf:<dataset>/<config>/<split>/<row_idx>",
    },
    collections: [{ ident: "train", name: "train", rows: 3 }],
    samples: {
      train: [{ trace_id: "trace-1", text: "debug CLI failure" }],
    },
    constraints: {
      dbIsImmutable: true,
      libIsTenantWritable: true,
      finalAnswerRequiresDfAnswer: true,
      scripts: {
        scratch: "explore",
        answer: "commit",
      },
    },
  };
}

async function startFakeHfServer(): Promise<{ url: string; close: () => Promise<void> }> {
  const rows = [
    { trace_id: "trace-1", text: "write docs", label: "docs" },
    { trace_id: "trace-2", text: "debug CLI failure", label: "debug" },
    { trace_id: "trace-3", text: "review release", label: "review" },
  ];
  const server = http.createServer((req, res) => {
    const url = new URL(req.url ?? "/", "http://127.0.0.1");
    res.setHeader("content-type", "application/json");
    if (url.pathname === "/splits") {
      res.end(
        JSON.stringify({
          splits: [
            {
              dataset: "OpenTraces/opentraces-devtime",
              config: "default",
              split: "train",
            },
          ],
        }),
      );
      return;
    }
    if (url.pathname === "/info") {
      res.end(
        JSON.stringify({
          dataset_info: {
            default: {
              license: "cc-by-4.0",
              description: "Small trace dataset for datafetch tests.",
              splits: {
                train: { name: "train", num_examples: rows.length },
              },
            },
          },
        }),
      );
      return;
    }
    if (url.pathname === "/rows") {
      const offset = Number(url.searchParams.get("offset") ?? "0");
      const length = Number(url.searchParams.get("length") ?? "10");
      res.end(
        JSON.stringify({
          rows: rows.slice(offset, offset + length).map((row, i) => ({
            row_idx: offset + i,
            row,
          })),
          num_rows_total: rows.length,
        }),
      );
      return;
    }
    if (url.pathname === "/search") {
      const query = (url.searchParams.get("query") ?? "").toLowerCase();
      const matched = rows
        .map((row, i) => ({ row, row_idx: i }))
        .filter(({ row }) => JSON.stringify(row).toLowerCase().includes(query));
      res.end(JSON.stringify({ rows: matched }));
      return;
    }
    res.statusCode = 404;
    res.end(JSON.stringify({ error: "not_found" }));
  });
  await new Promise<void>((resolve) => server.listen(0, "127.0.0.1", resolve));
  const address = server.address();
  if (address === null || typeof address === "string") {
    throw new Error("expected TCP fake HF server address");
  }
  return {
    url: `http://127.0.0.1:${address.port}`,
    close: () =>
      new Promise<void>((resolve, reject) => {
        server.close((err) => (err ? reject(err) : resolve()));
      }),
  };
}
